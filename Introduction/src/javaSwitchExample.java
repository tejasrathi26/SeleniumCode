public class javaSwitchExample{
    //Example to find whether the letter is vowel letter or not
    public static void main(String[] args) {

        char c = 'o';

        switch(c){
            case 'a':
                System.out.println(c +" It is a vowel");
                break;

            case 'e':
                System.out.println(c +" It is a vowel");
                break;

            case 'i':
                System.out.println(c +" It is a vowel");
                break;

            case 'o':
                System.out.println(c +" It is a vowel");
                break;

            case 'u':
                System.out.println(c +" It is a vowel");
                break;

            default:
                System.out.println(c +" is Not a Vowel");
        }

    }
}
